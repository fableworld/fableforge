fn encode(input: impl io::Read + io::Seek, output: impl io::Write, collection: usize, track: usize) -> anyhow::Result<()> {
    let mut tag = Tag::read_from2(input)?;
    tag.set_title(format!("K{collection:04}CP{track:02}"));

    let writer = MkiScrambledWriter::new(output);
    tag.write_to(writer, Version::Id3v24)?;
    Ok(())
}

pub async fn encode_using_tempfile(input: impl AsRef<Path>, output: impl AsRef<Path>, collection: usize, track: usize) -> anyhow::Result<()> {
    let input_bytes = std::fs::read(&input)?;
    let mut tag = Tag::read_from2(Cursor::new(&input_bytes))?;
    tag.set_title(format!("K{collection:04}CP{track:02}"));

    let temp_out = env::temp_dir().join(format!("{}_faba.mp3", SystemTime::now().duration_since(UNIX_EPOCH).unwrap().as_millis()));
    tokio::fs::copy(&input, &temp_out).await?;
    tag.write_to_path(&temp_out, Version::Id3v23)?;

    let bytes = fs::read(&temp_out)?;
    fs::remove_file(temp_out)?;

    let unscrambled = scramble(bytes).collect::<Vec<_>>();
    tokio::fs::write(output, unscrambled).await?;

    Ok(())
}

pub fn encode_bytes_using_tempfile(input: &[u8], output: impl AsRef<Path>, collection: usize, track: usize) -> anyhow::Result<()> {
    let mut tag = Tag::read_from2(Cursor::new(input))?;
    tag.set_title(format!("K{collection:04}CP{track:02}"));

    let temp_out = env::temp_dir().join(format!("{}_faba.mp3", SystemTime::now().duration_since(UNIX_EPOCH).unwrap().as_millis()));
    fs::write(&temp_out, &input)?;
    tag.write_to_path(&temp_out, Version::Id3v23)?;

    let bytes = fs::read(&temp_out)?;
    fs::remove_file(temp_out)?;

    let unscrambled = scramble(bytes).collect::<Vec<_>>();
    fs::write(output, unscrambled)?;

    Ok(())
}

fn scramble<'x, In>(instream: In) -> impl Iterator<Item=u8> + 'x
    where In: IntoIterator,
          In::IntoIter: 'x,
          In::Item: Borrow<u8>,
{
    instream.into_iter()
        .enumerate()
        .map(|(idx, byte)| RAINBOW_TABLE[idx % 4][*byte.borrow() as usize])
}

fn unscramble<'x, In>(instream: In) -> impl Iterator<Item=u8> + 'x
    where In: IntoIterator,
          In::IntoIter: 'x,
          In::Item: Borrow<u8>,
{
    instream.into_iter()
        .enumerate()
        .map(|(idx, byte)| REVERSE_RAINBOW_TABLE[idx % 4][*byte.borrow() as usize])
}

pub struct MkiScrambledWriter<W> {
    circ_idx: usize,
    inner: W,
}

impl<W> MkiScrambledWriter<W> {
    pub fn new(writer: W) -> Self {
        Self {
            circ_idx: 0,
            inner: writer,
        }
    }
}

impl<W> io::Write for MkiScrambledWriter<W>
    where W: io::Write
{
    fn write(&mut self, buf: &[u8]) -> io::Result<usize> {
        let mut scrambled_buf = Vec::with_capacity(buf.len());
        for byte in buf {
            scrambled_buf.push(RAINBOW_TABLE[self.circ_idx][*byte as usize]);
            self.circ_idx = (self.circ_idx + 1) % 4;
        }
        println!("writing {} bytes", scrambled_buf.len());
        self.inner.write(&scrambled_buf)
    }

    fn flush(&mut self) -> io::Result<()> {
        self.inner.flush()
    }
}

